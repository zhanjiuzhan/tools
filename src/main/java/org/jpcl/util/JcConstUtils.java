final public class JcConstUtils {

    private JcConstUtils() {}

    // 文件描述符
    /**
     * 一个tab键
     */
    public static final String KEY_TAB = "\t";

    /**
     * 在文中该处换行
     */
    public static final String KEY_NLINE = "\n";

    /**
     * 在文中该处插入回车
     */
    public static final String KEY_ENTER = "\r";

    /**
     * 在文中该处插入换页符
     */
    public static final String KEY_SKIP = "\f";

    /**
     * window系统文件分个符
     */
    public static final String WIN_SEP = "\\";

    /**
     * Linux系统文件分个符
     */
    public static final String LIN_SEP = "/";

    public static final int N1204 = 1204;

    public static final int N256 = 256;

    public static final int N32 = 32;
}
